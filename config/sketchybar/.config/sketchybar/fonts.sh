#!/bin/bash

# Shared fonts. One family, semantic names per use so sizes/weights live in a
# single place. Sourced by sketchybarrc.

export FONT_FAMILY="Hack Nerd Font"

export FONT_ICON="$FONT_FAMILY:Bold:15.0"          # default item icons
export FONT_LABEL="$FONT_FAMILY:Regular:13.0"      # default item labels
export FONT_WORKSPACE="$FONT_FAMILY:Regular:24.0"  # aerospace workspace glyphs
export FONT_WINDOW_COUNT="$FONT_FAMILY:Regular:10.0" # window count
export FONT_MODE="$FONT_FAMILY:Bold:18.0"          # mode indicator glyph
export FONT_NET_LABEL="$FONT_FAMILY:Regular:9.0"   # network up/down labels
export FONT_NET_ICON="$FONT_FAMILY:Regular:10.0"   # network up/down icons
