find_in_directory() {
  dir=$(find $1 -type d -not -path '*/\.*' -maxdepth 2 | fzf)
  cd $dir
  if [ ! -z "$2" ] && [ "$2" = "--edit" ]; then
    eval "$EDITOR ."
  fi
  starship_precmd
  zle reset-prompt
}

change_source_directory() {
  find_in_directory $CODE
}

edit_source_directory() {
  find_in_directory $CODE --edit
}
