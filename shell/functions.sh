# @see https://danishpraka.sh/2018/08/03/command-line-dir-switcher.html
find_in_directory() {
  cd $(find $1 -type d -not -path '*/\.*' -maxdepth 2 | fzf)
  starship_precmd
  zle reset-prompt
}

change_source() {
  find_in_directory $CODE
}

edit_source() {
  find_in_directory $CODE
  eval "$EDITOR ."
}
