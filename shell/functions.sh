change_source() {
  SOURCE_DIR="$CODE/$*"
  eval "cd $SOURCE_DIR"
}

edit_source() {
  SOURCE_DIR="$CODE/$*"
  eval "cd $SOURCE_DIR && $EDITOR $SOURCE_DIR"
}

# @see https://danishpraka.sh/2018/08/03/command-line-dir-switcher.html
find_source() {
  cd $(find ~/Code -type d -not -path '*/\.*' -maxdepth 1 | fzf)
  starship_precmd
  zle reset-prompt
}
